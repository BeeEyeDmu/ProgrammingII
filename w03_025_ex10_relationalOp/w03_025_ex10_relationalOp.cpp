#include <stdio.h>

int main()
{
	int x = 1;
	int y = 2;

	// 관계연산자의 결과는 참(1) 또는 거짓(0)
	printf("x > y = %d\n", x > y);
	printf("x < y = %d\n", x < y);
	printf("x >= y = %d\n", x >= y);
	printf("x <= y = %d\n", x <= y);
	printf("x == y = %d\n", x == y);
	printf("x != y = %d\n", x != y);
}